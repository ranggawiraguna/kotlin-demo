fun main(){
	// Comparison Operators
	val a = 10
	val b = 20
	val c = 10

	println("a\t\t: $a")
	println("b\t\t: $b")
	println("c\t\t: $c")
	println("a == b\t: " + (a == b))
	println("a == c\t: " + (a == c))
	println("a != b\t: " + (a != b))
	println("a != c\t: " + (a != c))
	println("a > b\t: " + (a > b))
	println("a < b\t: " + (a < b))
	println("a >= b\t: " + (a >= b))
	println("a <= b\t: " + (a <= b))
	println("a >= c\t: " + (a >= c))
	println("a <= c\t: " + (a <= c))
}